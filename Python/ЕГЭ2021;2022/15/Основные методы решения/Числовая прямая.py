'''
На числовой прямой даны два отрезка: P=[4,15] и Q=[12,20]. 
Укажите наименьшую возможную длину такого отрезка A, что формула
((x ∈ P) ∧ (x ∈ Q)) → (x ∈ A)
тождественно истинна, то есть принимает значение 1 при любом значении переменной х. 
'''

p = [i/10 for i in range(4*10, 15*10+1)] #делаем массив из дробных массив для точности вычислений интервалов, промежутков....
q = [i/ 10 for i in range(12*10+1, 20*10+1)] # P.S. так надо(((((((

def function_100_ballov(x, a):
    return (( x in p) and ( x in q)) <= (x in a)

a = set() # как и в множестве. Для минимального- минимальный
for x in [i/ 10 for i in range(1, 10000)]:
    if not function_100_ballov(x, a):
        a.add(x)
print(sorted(a)) # 12.1 => число 12 не берется, т.к. находится СЛЕВА, это простая информатика
