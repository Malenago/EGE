'''
На числовой прямой даны два отрезка: P = [8, 39] и Q = [23, 58].
Какова наименьшая возможная длина интервала A, при которой выражение
((x ∈ P) ∨ (x ∈ А)) → ((x ∈ Q) ∨ (x ∈ А))

тождественно истинно, то есть принимает значение 1 при любом значении переменной х.
Ответ: 15.
'''

P = [i/10 for i in range(80, 390+1)]
Q = [i/10 for i in range(230, 580+1)]

def wtf(x, a):
    return (( x in P ) or (x in A)) <= ((x in Q) or (x in A))

A = set()
for x in [i/10 for i in range(1, 1000)]:
    if not wtf(x, a):
        A.add(x)
print(sorted(A)) # 22.9 включается, т.к. находится СПРАВА. Т.Е. 23 - 8 = 15 длина



